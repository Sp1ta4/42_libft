#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	i;

	i = ft_strlen(s) - 1;
	while (s[i] != '\0' && s[i] != c)
	{
		i--;
	}
	return ((char *)&s[i]);
}
